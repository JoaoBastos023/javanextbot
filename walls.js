import * as THREE from 'three';

// Helper: Create a Box3 from two corner coordinates
function createBoxFromCorners(p1, p2) {
    const min = new THREE.Vector3(
        Math.min(p1[0], p2[0]),
        Math.min(p1[1], p2[1]),
        Math.min(p1[2], p2[2])
    );

    const max = new THREE.Vector3(
        Math.max(p1[0], p2[0]),
        Math.max(p1[1], p2[1]),
        Math.max(p1[2], p2[2])
    );

    return new THREE.Box3(min, max);
}

